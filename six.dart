//Q.6: Write a program to check whether an alphabet is a vowel or consonant.
void main() {
  String alphabet = "b";
  if (alphabet == 'a' ||
      alphabet == 'e' ||
      alphabet == 'i' ||
      alphabet == 'o' ||
      alphabet == 'u') {
    print("\nIT IS A VOWEL WORD");
  } else {
    print("\nIT IS A CONSONENT WORD");
  }
}
